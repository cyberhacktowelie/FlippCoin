static void draw_image(Canvas* canvas, const bool image[][128], uint8_t x, uint8_t y) {
    for(uint8_t i = 0; i < 64; i++) {
        for(uint8_t j = 0; j < 128; j++) {
            if(image[i][j]) {
                canvas_draw_box(canvas, x + j, y + i, 1, 1);
            }
        }
    }
}

static void app_draw_callback(Canvas* canvas, void* ctx) {
    UNUSED(ctx);

    canvas_clear(canvas);

    if(coin_state == CoinStateWaiting) {
        canvas_draw_str(canvas, 35, 30, "Press to flip");
        canvas_draw_str(canvas, 30, 40, "Black / White");
    } else if(coin_state == CoinStateFlipping) {
        // Draw flipping animation
        static uint8_t animation_frame = 0;
        if(animation_frame % 2 == 0) {
            draw_image(canvas, dolphin_white, 0, 0);
        } else {
            draw_image(canvas, dolphin_black, 0, 0);
        }
        animation_frame++;
    } else if(coin_state == CoinStateShowingResult) {
        if(coin_result) {
            draw_image(canvas, dolphin_white, 0, 0); // Dolphin white
        } else {
            draw_image(canvas, dolphin_black, 0, 0); // Dolphin black
        }
    }
}

static void app_input_callback(InputEvent* input_event, void* ctx) {
    furi_assert(ctx);

    FuriMessageQueue* event_queue = ctx;
    bool app_exit = false;

    if(input_event->type == InputTypePress) {
        if(input_event->key == InputKeyBack) {
            app_exit = true;
        } else if(input_event->key == InputKeyOk) {
            app_exit = false;
        } else {
            return; // No envíe ningún evento si no se presionan los botones OK o atrás
        }

        furi_message_queue_put(event_queue, &app_exit, FuriWaitForever);
    }
}

int32_t coin_flip_main(void* p) {
    UNUSED(p);
    FuriMessageQueue* event_queue = furi_message_queue_alloc(8, sizeof(bool));

    ViewPort* view_port = view_port_alloc();
    view_port_draw_callback_set(view_port, app_draw_callback, view_port);
    view_port_input_callback_set(view_port, app_input_callback, event_queue);

    Gui* gui = furi_record_open(RECORD_GUI);
    gui_add_view_port(gui, view_port, GuiLayerFullscreen);

    bool app_exit = false;

    while(!app_exit) {
        if(furi_message_queue_get(event_queue, &app_exit, FuriWaitForever) == FuriStatusOk) {
            if(app_exit) {
                break;
            }

            if(coin_state == CoinStateWaiting) {
                coin_state = CoinStateFlipping;

                for(int i = 0; i < 10; i++) {
                    furi_delay_ms(200);
                    view_port_update(view_port);
                }

                coin_result = rand() % 2;
                coin_state = CoinStateShowingResult;
            } else if(coin_state == CoinStateShowingResult) {
                coin_state = CoinStateWaiting;
            }
        }
        view_port_update(view_port);
    }

    view_port_enabled_set(view_port, false);
    gui_remove_view_port(gui, view_port);
    view_port_free(view_port);
    furi_message_queue_free(event_queue);

    furi_record_close(RECORD_GUI);

    return 0;
}
